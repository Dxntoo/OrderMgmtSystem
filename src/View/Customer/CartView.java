    private void btnCheckoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckoutActionPerformed
        // TODO add your handling code here:
        
        int dialogResult = JOptionPane.showConfirmDialog(null, "Do you want to confirm purchase?", "Confirm",
        JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        switch (dialogResult) {
            case JOptionPane.YES_OPTION:
                // Saving code here
                double totalprice = 0;
                for(int i = 0; i < model.getRowCount(); i++){
                double price = Double.parseDouble((String)model.getValueAt(i,5).toString());
                
                totalprice += price;
                if(i == model.getRowCount() - 1){
                  if(customer != null){
                    Orders o = new Orders(customer, cart.getCartItems(),totalprice);//creates a new order instance and passes customer obj and cart arraylist 
                    new OrderManager().addOrder(o);//adds o to the addorder method in OrderManager
                    cart.clearItems();
                    JOptionPane.showMessageDialog(this, "Purchase successful!");
                    this.dispose();
                    new CustomerMenuView(customer, cart).setVisible(true);
                }else{
                    JOptionPane.showMessageDialog(this, "An error occured, please log in again.");
                    this.dispose();
                    new LoginView().setVisible(true);
                }     
                    
                    
                }
            }
                
                break;
            case JOptionPane.NO_OPTION:
                return;
            case JOptionPane.CLOSED_OPTION:
                return;
            default:
                break;
        }
    }//GEN-LAST:event_btnCheckoutActionPerformed

