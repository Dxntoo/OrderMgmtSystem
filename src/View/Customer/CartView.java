    public void loadTable(){
        model = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column){
               return false;
            }
        };
        model.addColumn("Product ID");
        model.addColumn("Product");
        model.addColumn("Price Per Unit (RM)");
        model.addColumn("Type");
        model.addColumn("Quantity");
        model.addColumn("Total Price (RM)");
        
        if(cart != null){
            if(cart.getCartItems().isEmpty()){
                JOptionPane.showMessageDialog(this, "The cart is empty.");
                btnRemove.setEnabled(false);
                btnEditQuantity.setEnabled(false);
                btnCheckout.setEnabled(false);     
            }else{
                ArrayList<OrderedItems> cartItems = cart.getCartItems();
                double grandTotal = 0;
                for(int i = 0; i < cartItems.size(); i++){
                    String itemId = cartItems.get(i).getItems().getItemID();
                    String itemName = cartItems.get(i).getItems().getItemName();
                    String itemSize = cartItems.get(i).getItems().getSize();
                    double price = cartItems.get(i).getItems().getItemPrice();
                    int quantity = cartItems.get(i).getQuantity();
                    
                    double totalPrice = price * quantity;
                    grandTotal += totalPrice;
                    Object[] data = {itemId, itemName, price, itemSize, quantity, totalPrice};
                    model.addRow(data);
                    
                }
                lblTotalPrice.setText("RM" + Double.toString(grandTotal));
                
            }
                       
        }else{
            JOptionPane.showMessageDialog(this, "The cart is empty.");
            btnRemove.setEnabled(false);
            btnEditQuantity.setEnabled(false);
            btnCheckout.setEnabled(false);           
        }
        tblCart.setModel(model);
        
        
    }

    private void refreshTable(){
        if(model.getRowCount() > 0){
            for(int i = model.getRowCount() - 1; i >= 0 ; i--){
                model.removeRow(i);
            }
            loadTable();
        }
    }
